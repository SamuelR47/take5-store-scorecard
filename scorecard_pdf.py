"""Server-side one-page KPI score card as a real PDF (fpdf2). Returned as bytes for
st.download_button. KPI cards only - no graphs - matching the on-screen strip."""
from fpdf import FPDF

NAVY = (20, 39, 63); RED = (228, 0, 43); INK = (31, 42, 55)
MUTE = (91, 107, 127); GREEN = (30, 142, 78); LINE = (210, 217, 227)


def build_scorecard_pdf(store_name, code, date_str, headline, cards, sowhat_text=""):
    """cards: list of (label, value_str, sub_str, rgb_tuple)."""
    pdf = FPDF(orientation="P", unit="mm", format="Letter")
    pdf.set_auto_page_break(False)
    pdf.add_page()
    W = 215.9
    # header bar
    pdf.set_fill_color(*NAVY); pdf.rect(10, 10, W - 20, 20, "F")
    pdf.set_fill_color(*RED); pdf.rect(14, 14, 2.2, 12, "F")
    pdf.set_xy(20, 13); pdf.set_text_color(255, 255, 255)
    pdf.set_font("Helvetica", "B", 15); pdf.cell(0, 7, "VantEdge Auto  -  Store Scorecard", ln=1)
    pdf.set_xy(20, 21); pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(220, 229, 240)
    pdf.cell(0, 6, f"{store_name} ({code})   -   {date_str}", ln=1)
    y = 36
    if headline:
        pdf.set_xy(10, y); pdf.set_text_color(*INK); pdf.set_font("Helvetica", "B", 11)
        pdf.multi_cell(W - 20, 5.5, headline); y = pdf.get_y() + 4
    # KPI card grid (3 per row)
    cols, gap, m = 3, 5, 10
    cw = (W - 2 * m - (cols - 1) * gap) / cols
    ch = 26
    for i, (label, value, sub, rgb) in enumerate(cards):
        r, c = divmod(i, cols)
        x = m + c * (cw + gap); yy = y + r * (ch + gap)
        pdf.set_draw_color(*LINE); pdf.set_line_width(0.3)
        pdf.set_fill_color(255, 255, 255); pdf.rect(x, yy, cw, ch, "DF")
        pdf.set_fill_color(*rgb); pdf.rect(x, yy, cw, 1.6, "F")
        pdf.set_xy(x + 4, yy + 4); pdf.set_text_color(*MUTE); pdf.set_font("Helvetica", "", 8)
        pdf.cell(cw - 8, 4, label.upper())
        pdf.set_xy(x + 4, yy + 9); pdf.set_text_color(*INK); pdf.set_font("Helvetica", "B", 17)
        pdf.cell(cw - 8, 8, str(value))
        pdf.set_xy(x + 4, yy + 18); pdf.set_text_color(*rgb); pdf.set_font("Helvetica", "B", 8)
        pdf.cell(cw - 8, 4, sub or "")
    ny = y + ((len(cards) + cols - 1) // cols) * (ch + gap) + 4
    pdf.set_xy(10, ny); pdf.set_text_color(*MUTE); pdf.set_font("Helvetica", "I", 8)
    pdf.multi_cell(W - 20, 4, "Target = norm +10% (stretch). Pace and projection measured "
                              "against the true norm. Generated by the Take 5 Scorecard.")
    out = pdf.output()
    return bytes(out)
